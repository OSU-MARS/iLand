        public Nullable<double> CurrentSnagSwdDecompositionRate { get; private set; }
        public Nullable<double> CurrentSnagSwdHalfLife { get; private set; }
        public Nullable<double> CurrentSnagSwdN { get; private set; }

        public Nullable<double> CurrentSoilAvailableNitrogen { get; private set; }
        public Nullable<double> CurrentSoilDepth { get; private set; }
        public Nullable<double> CurrentSoilEr { get; private set; }
        public Nullable<double> CurrentSoilEl { get; private set; }
        public Nullable<double> CurrentSoilLeaching { get; private set; }
        public Nullable<double> CurrentSoilHumificationRate { get; private set; }
        public Nullable<double> CurrentSoilOrganicC { get; private set; }
        public Nullable<double> CurrentSoilOrganicDecompositionRate { get; private set; }
