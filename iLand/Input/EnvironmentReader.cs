        public Nullable<double> CurrentSoilQh { get; private set; }
        public Nullable<double> CurrentSoilSand { get; private set; }
        public Nullable<double> CurrentSoilSilt { get; private set; }
        public Nullable<double> CurrentSoilClay { get; private set; }
        public Nullable<double> CurrentSoilYoungLabileC { get; private set; }
        public Nullable<double> CurrentSoilYoungLabileDecompositionRate { get; private set; }
        public Nullable<double> CurrentSoilYoungLabileN { get; private set; }
        public Nullable<double> CurrentSoilYoungRefractoryC { get; private set; }
        public Nullable<double> CurrentSoilYoungRefractoryDecompositionRate { get; private set; }
